import React, { Suspense, useState } from "react";
import Head from "next/head";
import TodoList from "../components/TodoList";
import Image from "next/image";
import styles from "../styles/Home.module.css";
import { useFetchedTodos } from "../hooks/useFetchedTodos";
import supabase from "../utils/supabase";

const Home: React.FC = () => {
  const [title, setTitle] = useState("");
  const { todos, fetchTodos } = useFetchedTodos();
  const addTodo = async (e: React.FormEvent<HTMLFormElement>) => {
    e.preventDefault();
    await supabase.from("todos").insert({ title, completed: false });
    fetchTodos();
    setTitle("");
  };
  const handleChangeTitle = (e: React.ChangeEvent<HTMLInputElement>) => {
    setTitle(e.target.value);
  };
  const handleChangeCompleted = async (
    e: React.ChangeEvent<HTMLInputElement>
  ) => {
    e.preventDefault();
    const { error } = await supabase
      .from("todos")
      .update({ completed: e.target.checked })
      .eq("id", e.target.name);
    fetchTodos();
  };
  return (
    <div className={styles.container}>
      <Head>
        <title>Next.js Todo App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <header>
        <h1>Todo App</h1>
      </header>

      <main className={styles.main}>
        <h2>Todo一覧</h2>
        <form onSubmit={addTodo}>
          <input
            type="text"
            name="todoname"
            placeholder="Todo Name"
            value={title}
            onChange={handleChangeTitle}
          />
          <button type="submit">Add Todo</button>
        </form>
        {todos === null ? (
          <p>Loading...</p>
        ) : (
          <TodoList
            todos={todos}
            handleChangeCompleted={handleChangeCompleted}
          />
        )}
      </main>
    </div>
  );
};
export default Home;
